            double total_bill = Double.parseDouble(totalbill_lbl.getText());
            double full_total = Double.parseDouble(total) + total_bill;
            totalbill_lbl.setText(String.valueOf(full_total));
            
            //add details to table
            DefaultTableModel dtm =(DefaultTableModel)jTable1.getModel();
            String sid = saleId_lbl.getText();
            dtm.insertRow(dtm.getRowCount(), new Object[]{sid, brand , model, item, qty, price, total } );
            
