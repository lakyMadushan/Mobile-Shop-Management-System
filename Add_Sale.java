            rs.next();
            String id = rs.getString("id");
            int qty1 = Integer.parseInt(rs.getString("qty"));
            int qty2 = Integer.parseInt(qty);
            int qty3 = qty1 - qty2;
            dbcon.IUD("UPDATE inventory SET `qty` = '"+String.valueOf(qty3)+"' WHERE `id` = '"+id+"' ");
